from statistics import mean,median,mode
array_of_elements=eval(input("Enter the array numbers: "))
mean_value=mean(array_of_elements)
median_value=median(array_of_elements)
mode_value=mode(array_of_elements)
print(f"mean:{mean_value}")
print(f"median:{median_value}")
print(f"mode:{mode_value}")

